# Add parameter storage for Dynamixel#1 present position
for key, value in servos.items():
    dxl_addparam_result = groupBulkRead.addParam(value, ADDR_PRESENT_POSITION, LEN_PRESENT_POSITION)
    if dxl_addparam_result != True:
        print("[ID:%03d] groupBulkRead addparam failed" % value)
        quit()

while 1:
    print("Press any key to continue! (or press ESC to quit!)")
    if getch() == chr(0x1b):
        break

    while 1:
        # Bulkread present position and LED status
        dxl_comm_result = groupBulkRead.txRxPacket()
        if dxl_comm_result != COMM_SUCCESS:
            print("%s" % packetHandler.getTxRxResult(dxl_comm_result))
            continue

        angles = []
        for key, value in servos.items():
            # Check if groupbulkread data of Dynamixel#1 is available
            dxl_getdata_result = groupBulkRead.isAvailable(value, ADDR_PRESENT_POSITION, LEN_PRESENT_POSITION)
            if dxl_getdata_result != True:
                print("[ID:%03d] groupBulkRead getdata failed" % value)
                quit()

            start_addr = groupBulkRead.data_dict[value][PARAM_NUM_ADDRESS]
            current_position = bytes([
                groupBulkRead.data_dict[value][PARAM_NUM_DATA][ADDR_PRESENT_POSITION - start_addr + 0],
                groupBulkRead.data_dict[value][PARAM_NUM_DATA][ADDR_PRESENT_POSITION - start_addr + 1],
                groupBulkRead.data_dict[value][PARAM_NUM_DATA][ADDR_PRESENT_POSITION - start_addr + 2],
                groupBulkRead.data_dict[value][PARAM_NUM_DATA][ADDR_PRESENT_POSITION - start_addr + 3]])

            position_signed = int.from_bytes(current_position, "little", signed=True)
            current_angle = position_signed * 0.088
            current_angle = round(current_angle, 2)
            angles.append(current_angle)

        print(*angles)

# Clear bulkread parameter storage
groupBulkRead.clearParam()

